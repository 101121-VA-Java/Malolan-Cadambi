package Calculator;

public class Calculator{

    public int add(int a, int b){
        int c = a + b;
        return c;
    }

    public int subtract(int a, int b){
        int c = a - b;
        return c;
    }
    //multipy

    public double multiply(double a, double b){
        double c = a * b;
        return c;
    }

    //Reminder

    public int remainder(int a, int b){
        int c = a / b;
        return c;
    }
}